#!/usr/bin/env python3
"""Regression tests for pdf_inventory.py + pdf_extract.py.

Strategy: ship four tiny fixture PDFs under
``test_data/pdf_inventory_fixture/`` (under 10 KB total) that exercise
every classification branch the inventory needs. The fixtures are
generated by ``_make_fixtures.py`` (committed prebuilt so CI doesn't
need ``reportlab``); regenerating requires reportlab but verifying
pyests do not.

Test coverage:

  * ``pdf_inventory.discover_pdfs`` glob filtering and stable order.
  * ``pdf_inventory.classify_pdf`` for all four classification
    branches (text_extractable, image_only, corrupted, plus a
    no-metadata variant of text_extractable).
  * Inventory writer produces one JSONL row per file with the
    documented schema.
  * Privacy guard refuses non-private outputs unless overridden.
  * pdf_extract reads the inventory, validates impostor fields,
    invokes preprocessing, and emits a manifest entry that
    validates clean.
  * pdf_extract skips inventory rows missing user-supplied
    impostor fields rather than emitting an invalid manifest.
  * pdf_extract dedupes by content_hash within author subdir.
  * pdf_extract skips ``corrupted`` rows from the inventory.
  * OCR unavailability is announced once + recorded as a skip; tests
    pass whether or not ocrmypdf + tesseract + ghostscript are
    installed.
"""


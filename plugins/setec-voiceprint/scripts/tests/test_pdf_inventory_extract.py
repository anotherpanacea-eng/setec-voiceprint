def test_extract_skips_corrupted_inventory_rows(tmp_path):
    """Inventory rows with classification=corrupted skip the extract
    step entirely; the script logs the skip but doesn't try to
    open the bad file."""
    inv = tmp_path / "ai-prose-baselines-private" / "inv3.jsonl"
    pi.run(make_inventory_args(output=str(inv)))
    rows = read_jsonl(inv)
    augmented = _augment_inventory_for_extract(rows)
    # Force the corrupted row to also have impostor fields populated
    # so we know its skip is for the corrupted reason, not metadata.
    for r in augmented:
        if r["classification"] == "corrupted":
            r.update({
                "persona": "synthetic_author_personal",
                "register": "academic_philosophy",
                "register_match": "high",
                "topic_match": "medium",
                "consent_status": "fair_use_research",
                "era": "pre_chatgpt",
                "impostor_for": ["blog"],
            })
    inv_filtered = tmp_path / "ai-prose-baselines-private" / "inv3_aug.jsonl"
    _write_filtered_inventory(augmented, inv_filtered)

    out_dir = tmp_path / "ai-prose-baselines-private" / "extracted3"
    args = make_extract_args(
        inventory=str(inv_filtered), output_dir=str(out_dir),
        emit_manifest=str(out_dir / "draft.jsonl"),
    )
    pe.run(args)
    # No corrupted-source file in the output. Only 1 text-extractable
    # text wins after dedupe.
    txts = list(out_dir.rglob("*.txt"))
    assert len(txts) == 1
    body = txts[0].read_text(encoding="utf-8")
    assert "discipline of attention" in body.lower()



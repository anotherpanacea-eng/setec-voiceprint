def test_extract_skip_ocr_skips_image_only(tmp_path):
    """When --skip-ocr is set, image_only rows skip extraction
    cleanly (no spurious error)."""
    inv = tmp_path / "ai-prose-baselines-private" / "inv5.jsonl"
    pi.run(make_inventory_args(output=str(inv)))
    rows = read_jsonl(inv)
    # Augment image_only entry with impostor fields so the skip
    # reason is OCR availability, not missing metadata.
    augmented = []
    for r in rows:
        if r["classification"] == "image_only":
            r.update({
                "persona": "synthetic_author_personal",
                "register": "academic_philosophy",
                "register_match": "high",
                "topic_match": "medium",
                "consent_status": "fair_use_research",
                "era": "pre_chatgpt",
                "impostor_for": ["blog"],
            })
        augmented.append(r)
    inv_aug = tmp_path / "ai-prose-baselines-private" / "inv5_aug.jsonl"
    _write_filtered_inventory(augmented, inv_aug)

    out_dir = tmp_path / "ai-prose-baselines-private" / "skip_ocr"
    args = make_extract_args(
        inventory=str(inv_aug),
        output_dir=str(out_dir),
        emit_manifest=str(out_dir / "draft.jsonl"),
        skip_ocr=True,
    )
    rc = pe.run(args)
    # No OCR output for the image_only row.
    txts = list(out_dir.rglob("*.txt"))
    # No txt comes from image_only because we --skip-ocr.
    # The text-extractable row may produce one txt before dedupe-only
    # if persona-field augmentation was applied to it; in this test
    # we only augmented image_only, so text rows skip on missing
    # impostor fields.
    # The assertion: the run did not crash on the image_only entry.
    assert isinstance(rc, int)
    # No image_only-derived file in output (image_only PDF text
    # would have been skipped due to skip_ocr).



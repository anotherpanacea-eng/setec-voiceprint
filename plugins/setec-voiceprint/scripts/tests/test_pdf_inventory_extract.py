def test_extracted_manifest_passes_validator(tmp_path):
    """Extracted draft manifest validates clean once an
    identity_baseline entry naming the impostor target persona
    is appended (mirroring acquire_blog's validator-integration
    test)."""
    inv = tmp_path / "ai-prose-baselines-private" / "inv_v.jsonl"
    pi.run(make_inventory_args(output=str(inv)))
    rows = read_jsonl(inv)
    augmented = _augment_inventory_for_extract(rows)
    inv_aug = tmp_path / "ai-prose-baselines-private" / "inv_v_aug.jsonl"
    _write_filtered_inventory(augmented, inv_aug)

    out_dir = tmp_path / "ai-prose-baselines-private" / "vt"
    manifest_path = out_dir / "draft.jsonl"
    args = make_extract_args(
        inventory=str(inv_aug),
        output_dir=str(out_dir),
        emit_manifest=str(manifest_path),
    )
    rc = pe.run(args)
    assert rc == 0

    # Add an identity_baseline entry naming "blog" so the cross-check
    # has a target persona.
    baseline = out_dir / "synthetic-author-personal" / "fake_baseline.txt"
    baseline.write_text("Identity baseline prose. " * 100, encoding="utf-8")
    baseline_entry = {
        "id": "fake_baseline",
        "path": str(baseline.relative_to(manifest_path.parent)),
        "author": "Test User",
        "persona": "blog",
        "register": "academic_philosophy",
        "ai_status": "pre_ai_human",
        "language_status": "native",
        "use": ["baseline", "voice_profile"],
        "split": "baseline",
        "privacy": "private",
        "corpus_role": "identity_baseline",
        "era": "pre_chatgpt",
    }
    with manifest_path.open("a", encoding="utf-8") as f:
        f.write(json.dumps(baseline_entry, sort_keys=True) + "\n")

    report = mv.validate_manifest(manifest_path)
    error_issues = [
        i for i in report["issues"] if i.get("severity") == "error"
    ]
    assert error_issues == [], \
        f"Extracted manifest should validate without errors: {error_issues}"



def test_fixture_pdfs_exist():
    """All four fixture PDFs are committed; the tests below depend
    on them. If any are missing, regenerate via _make_fixtures.py.
    """
    for p in (PDF_WITH_META, PDF_NO_META, PDF_IMAGE_ONLY, PDF_CORRUPTED):
        assert p.is_file(), f"missing fixture: {p}"
    # Total fixture size stays under 1 MB per spec.
    total = sum(p.stat().st_size for p in (
        PDF_WITH_META, PDF_NO_META, PDF_IMAGE_ONLY, PDF_CORRUPTED,
    ))
    assert total < 1024 * 1024, f"fixture total {total} bytes > 1 MB cap"


# ------------------- pdf_inventory unit tests --------------------


def test_classify_text_yield_branches():
    """All three thresholds map to the documented classification."""
    assert pi._classify_text_yield(500) == ("text_extractable", True, False)
    assert pi._classify_text_yield(0) == ("image_only", False, True)
    assert pi._classify_text_yield(50) == ("mixed", False, True)


def test_metadata_quality_buckets():
    assert pi._metadata_quality("T", "A", "2020-01-01") == "good"
    assert pi._metadata_quality(None, "A", "2020-01-01") == "partial"
    assert pi._metadata_quality(None, None, None) == "none"


def test_normalize_pdf_date_parses_pdf_format():
    """PDF spec dates are ``D:YYYYMMDDHHmmSS+OO'mm'``; ISO output."""
    assert pi._normalize_pdf_date("D:20200304120000+00'00'") == "2020-03-04"
    assert pi._normalize_pdf_date("D:20200304") == "2020-03-04"
    assert pi._normalize_pdf_date("20200304120000") == "2020-03-04"
    assert pi._normalize_pdf_date(None) is None
    assert pi._normalize_pdf_date("not-a-date") is None


def test_classify_text_layer_with_metadata():
    """Born-digital PDF with title/author should classify as
    text_extractable, metadata_quality good, multi-page."""
    entry = pi.classify_pdf(PDF_WITH_META)
    assert entry.classification == "text_extractable"
    assert entry.extractable is True
    assert entry.needs_ocr is False
    assert entry.title == "On the Discipline of Attention"
    assert entry.author == "Synthetic Author"
    assert entry.metadata_quality == "good"
    assert entry.page_count >= 1
    assert entry.estimated_words > 0
    assert entry.file_hash.startswith("sha256:")
    assert entry.sample_chars_extracted > 100


def test_classify_text_layer_without_metadata():
    """Same content, no title/author: still text_extractable but
    metadata_quality drops to partial (creation-date is auto-set
    by reportlab)."""
    entry = pi.classify_pdf(PDF_NO_META)
    assert entry.classification == "text_extractable"
    assert entry.title is None
    assert entry.author is None
    assert entry.metadata_quality in ("partial", "none")


def test_classify_image_only():
    """A page with vector drawings and no text operators must
    classify as image_only (zero chars extracted)."""
    entry = pi.classify_pdf(PDF_IMAGE_ONLY)
    assert entry.classification == "image_only"
    assert entry.extractable is False
    assert entry.needs_ocr is True
    assert entry.sample_chars_extracted == 0


def test_classify_corrupted_does_not_raise():
    """A malformed PDF must classify as corrupted with notes set,
    not raise. The notes carry the exception class so the user
    can spot category failures."""
    entry = pi.classify_pdf(PDF_CORRUPTED)
    assert entry.classification == "corrupted"
    assert entry.notes is not None and entry.notes != ""
    assert entry.extractable is False
    assert entry.estimated_words == 0



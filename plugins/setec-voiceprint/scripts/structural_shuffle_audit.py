#!/usr/bin/env python3
"""structural_shuffle_audit.py — perplexity shift under sentence/word shuffling.

A Surface-5 discrimination-evidence capability that probes how much a text's
predictability depends on the *ordering* of its parts. Following the
Luminol-AIDetect method (arXiv:2604.25860, 2026 — **PROVISIONAL / UNVERIFIED
preprint**; see the prior-art fallback below), it scores three variants of a
window under a single causal LM:

  * ``ppl_orig`` — the text at its natural ordering,
  * ``ppl_sent`` — the same text with its sentences randomly permuted,
  * ``ppl_word`` — the same text with all its words randomly permuted,

and reports 5 scalar features describing the perplexity *jump* under shuffling.

The hypothesis (the paper's, the SETEC experiment confirms it empirically):
human prose draws meaning from syntactic and narrative flow, so permuting it
spikes perplexity; AI prose generated by next-token prediction has shallower
inter-sentence dependencies, so shuffling hurts it less *relative to itself* —
a smaller jump. The signal is therefore a **structural, order-dependence
axis**, orthogonal in mechanism to the natural-ordering probability family
(Binoculars cross-perplexity ratio, Fast-DetectGPT curvature, DivEye surprisal
moments), and generator-agnostic by construction.

Implements ``specs/32-structural-shuffle-perplexity.md`` (M1: model-free
orchestration). The model load is the M2 seam; M1 is CPU-only and CI-testable
against an injected (stub) backend.

Posture (no-verdict / anti-Goodhart)
------------------------------------
Ships ``status: heuristic`` with NO threshold and NO verdict band — it reports
a measurement, never an is_ai/is_human label. The 5 features are descriptive
evidence under one scorer model; any AI-likely / human-likely call needs
operator-supplied thresholds applied downstream. This audit is a COMPARISON /
HEURISTIC signal and must NEVER be the held-out SETEC fitness / validation
target (the anti-Goodhart separation is load-bearing). ``test_separation_guard``
and ``test_orthogonal_to_binoculars`` pin the import boundary.

Sign / direction
----------------
Higher ``ppl_diff`` / ``ppl_ratio`` / ``ppl_log_ratio`` / ``ppl_pct_change`` =
a larger perplexity jump under shuffling = **more order-dependent = more
human-like** (the hypothesis direction). Silent sign inversion is the
perturbation family's shared failure mode, so the direction is pinned by
``test_feature_direction_sign``. The direction is a hypothesis, NOT a verdict,
and the score ships uncalibrated.

Prior-art fallback
------------------
The shuffle-perplexity idea is not unique to Luminol. The perturbation-based
detection paradigm is established: DetectGPT (Mitchell et al. 2023,
arXiv:2301.11305) perturbs the *lexical* surface and measures the log-prob
curvature drop; this audit extends the same perturb-and-rescore paradigm to
*order* perturbation. If arXiv 2604.25860 turns out not to exist or to differ,
the method stands as an independent reimplementation of an order-perturbation
probe in the DetectGPT family — no paper-specific number is asserted in this
code.

CLI
---
    python3 plugins/setec-voiceprint/scripts/structural_shuffle_audit.py TARGET \\
        [--scorer gpt_neo_2_7b] [--seed INT] \\
        [--surprisal-dtype auto|fp32|fp16|bf16] [--device DEVICE] \\
        [--json] [--out PATH] [--out-md PATH]
"""

